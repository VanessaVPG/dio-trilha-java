public class Calculadora {
    public static void somar(double a, double b) {
        double resultado = a + b;
        System.out.println("A soma de " +a + "mais " + b + "é " + resultado);
    }
    public static void subtrair(double a, double b) {
        double resultado = a - b;
        System.out.println("A subtração de " + a + "menos " + b + "é " + resultado);
    };
    public static void multiplicar(double a, double b) {
        double resultado = a + b;
        System.out.println("A multiplicação de " +a + "por " + b + "é " + resultado);
    };
    public static void dividir(double a, double b) {
        double resultado = a / b;
        System.out.println("A divisão de " +a + "por " + b + "é " + resultado);
    };
}
